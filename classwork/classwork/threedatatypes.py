#python data types
a = 10
print(a)
print(type(a))    #integer
b=10.0
print(b) 
print(type(b))   #floating point number
c = "chaitanya"
print(c)
print(type(c))   #string
d = list([1,2,3])
print(d)
print(type(d))    #list
e = set(["i","t","g"])
print(e)
print(type(e))     #set
f = tuple([1,3,2,5,7])
print(f)
print(type(f))    #tuple
g = {"name":"chaitanya","reg" : "12"}
print(g)
print(type(g))   #dictionary
h = None
print(type(h))   #none
i = 1 + 9j
print(i)
print(type(i))  #complex number

